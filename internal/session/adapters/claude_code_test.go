// mustJSON marshals a string to a JSON string literal for embedding in test JSONL.
func mustJSON(s string) string {
	b, _ := json.Marshal(s)
	return string(b)
}

// parseLineFirst is a test helper that calls parseLine and returns the first entry,
// matching the old (*RawEntry, error) signature for tests that only expect one result.
func parseLineFirst(a *ClaudeCodeAdapter, line []byte) (*RawEntry, error) {
	entries, err := a.parseLine(line)
	if err != nil {
		return nil, err
	}
	if len(entries) == 0 {
		return nil, nil
	}
	return &entries[0], nil
}

