func TestClaudeCodeAdapter_ParseLine_AssistantMultiBlock(t *testing.T) {
	adapter := &ClaudeCodeAdapter{}

	t.Run("multi-block assistant message returns all entries", func(t *testing.T) {
		jsonl := `{
			"type": "assistant",
			"timestamp": "2026-01-05T10:00:00.000Z",
			"message": {
				"role": "assistant",
				"content": [
					{"type": "text", "text": "Let me read that file for you."},
					{
						"type": "tool_use",
						"id": "toolu_abc",
						"name": "Read",
						"input": {"file_path": "/src/main.go"}
					},
					{"type": "text", "text": "And also check the tests."},
					{
						"type": "tool_use",
						"id": "toolu_def",
						"name": "Read",
						"input": {"file_path": "/src/main_test.go"}
					}
				]
			}
		}`
		entries, err := adapter.parseLine([]byte(jsonl))
		require.NoError(t, err)
		require.Len(t, entries, 4, "should return all 4 content blocks")

		// first: text
		assert.Equal(t, "assistant", entries[0].Role)
		assert.Equal(t, "Let me read that file for you.", entries[0].Content)

		// second: tool_use
		assert.Equal(t, "tool", entries[1].Role)
		assert.Equal(t, "Read", entries[1].ToolName)
		assert.Contains(t, entries[1].ToolInput, "/src/main.go")

		// third: text
		assert.Equal(t, "assistant", entries[2].Role)
		assert.Equal(t, "And also check the tests.", entries[2].Content)

		// fourth: tool_use
		assert.Equal(t, "tool", entries[3].Role)
		assert.Equal(t, "Read", entries[3].ToolName)
		assert.Contains(t, entries[3].ToolInput, "/src/main_test.go")
	})

	t.Run("text and tool_use with thinking block skips thinking", func(t *testing.T) {
		jsonl := `{
			"type": "assistant",
			"timestamp": "2026-01-05T10:00:00.000Z",
			"message": {
				"role": "assistant",
				"content": [
					{"type": "thinking", "thinking": "I should check the file..."},
					{"type": "text", "text": "Here is the result."},
					{
						"type": "tool_use",
						"id": "toolu_ghi",
						"name": "Bash",
						"input": {"command": "ls"}
					}
				]
			}
		}`
		entries, err := adapter.parseLine([]byte(jsonl))
		require.NoError(t, err)
		require.Len(t, entries, 2, "should return text + tool_use, skip thinking")

		assert.Equal(t, "assistant", entries[0].Role)
		assert.Equal(t, "Here is the result.", entries[0].Content)

		assert.Equal(t, "tool", entries[1].Role)
		assert.Equal(t, "Bash", entries[1].ToolName)
	})
}

